package entities;

public class Registrador {
	
	private static double mbr;
	private static int mar;
	private static String ir;
	private static int pc = 1;

	
	public static double valueInMar() {
		return Memory.getMemoria()[mar];
	}
	
	public static void setMbr(double mbr) {
		Registrador.mbr = mbr;
	}
	public static double getMbr() {
		return mbr;
	}


	public static int getMar() {
		return mar;
	}

	public static void setMar(int mar) {
		Registrador.mar = mar;
	}

	public static String getIr() {
		return ir;
	}

	public static void modifyIr(int cod,int op1, int op2) {
		ir = cod+" "+op1+" "+op2;
	}

	public static int getPc() {
		return pc;
	}
	public static void setPc(int op1) {
		pc = op1;
	}

	public static void increasePc() {
		Registrador.pc += 1;
	}

}
